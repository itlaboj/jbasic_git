package p02;

public class Sample36 {

	public static void main(String[] args) {
		int[] list = {10, 20, 30};

		for (int i = 0; i < list.length; i++) {
			System.out.println(list[i]);
		}
		
		System.out.println();
		
		for (int i = list.length -1; i >= 0; i--) {
			System.out.println(list[i]);
		}
		
	}

}
